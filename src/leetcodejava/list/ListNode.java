package leetcodejava.list;

/**
 * 定义一个listNode节点
 *
 * @author zhangyu (zhangyuyu417@gmail.com)
 */
public class ListNode {
    /**
     * 节点存储的值
     */
    public int val;
    /**
     * 节点的指向
     */
    public ListNode next;

    /**
     * 构造方法
     *
     * @param x 节点传入的值
     */
    public ListNode(int x) {
        val = x;
    }

}
